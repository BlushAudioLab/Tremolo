juce::AudioProcessorValueTreeState::ParameterLayout AudioPluginAudioProcessor::createParameterLayout()
{
    juce::AudioProcessorValueTreeState::ParameterLayout layout;

    layout.add(std::make_unique<juce::AudioParameterFloat>("RATE",
                                                          "Rate",
                                                          juce::NormalisableRange<float>(0.1f, 20.0f, 0.01f),
                                                          5.0f,
                                                          juce::String(),
                                                          juce::AudioProcessorParameter::genericParameter,
                                                          [](float value, int) { return juce::String(value, 2) + "Hz"; },
                                                          [](const juce::String& text) { return text.dropLastCharacters(2).getFloatValue(); }));

    layout.add(std::make_unique<juce::AudioParameterFloat>("DEPTH",
                                                          "Depth",
                                                          juce::NormalisableRange<float>(0.0f, 1.0f, 0.01f),
                                                          0.5f,
                                                          juce::String(),
                                                          juce::AudioProcessorParameter::genericParameter,
                                                          [](float value, int) { return juce::String(value * 100, 0) + "%"; },
                                                          [](const juce::String& text) { return text.dropLastCharacters(1).getFloatValue() / 100.0f; }));

    return layout;
}

